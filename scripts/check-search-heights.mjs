import { chromium } from 'playwright'
const browser = await chromium.launch({ headless: true })
const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 } })
const page = await ctx.newPage()
await page.goto('http://localhost:5173/profiles', { waitUntil: 'networkidle' })
await page.waitForTimeout(800)
// Tags filter (FilterSelect searchable)
await page.locator('button:has-text("Tags")').first().click()
await page.waitForTimeout(300)
const tagsH = await page.evaluate(() => { const i = document.querySelector('[data-radix-popper-content-wrapper] input'); return i ? Math.round(i.getBoundingClientRect().height) : null })
await page.keyboard.press('Escape')
await page.waitForTimeout(200)
// Sort by (Select showSearch)
await page.locator('[data-slot="select-trigger"]').last().click()
await page.waitForTimeout(300)
const sortH = await page.evaluate(() => { const i = document.querySelector('[data-slot="select-search"]'); return i ? Math.round(i.getBoundingClientRect().height) : null })
console.log(JSON.stringify({ tagsSearchH: tagsH, sortSearchH: sortH }))
await browser.close()
